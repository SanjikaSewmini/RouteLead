package com.example.routelead.enums;

/**
 * Enum representing the different roles a user can have in the RouteLead system.
 */
public enum UserRole {
    /**
     * Administrator role with full system access
     */
    ADMIN,
    
    /**
     * Driver role for vehicle operators
     */
    DRIVER,
    
    /**
     * Customer role for service users
     */
    CUSTOMER
} 