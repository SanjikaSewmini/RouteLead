package com.example.routelead.enums;

/**
 * Enum representing the different entity types that can be managed by administrators in the RouteLead system.
 */
public enum AdminEntityType {
    /**
     * User entity type
     */
    USER,
    
    /**
     * Route entity type
     */
    ROUTE,
    
    /**
     * Bid entity type
     */
    BID,
    
    /**
     * Dispute entity type
     */
    DISPUTE
} 