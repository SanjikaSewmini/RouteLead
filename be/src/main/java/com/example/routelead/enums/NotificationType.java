package com.example.routelead.enums;

/**
 * Enum representing the different types of notifications in the RouteLead system.
 */
public enum NotificationType {
    /**
     * Notification for bid status updates
     */
    BID_UPDATE,
    
    /**
     * Notification for booking confirmations
     */
    BOOKING_CONFIRMED,
    
    /**
     * Notification for delivery status changes
     */
    DELIVERY_STATUS,
    
    /**
     * Notification for route changes
     */
    ROUTE_CHANGED,
    
    /**
     * Notification for dispute alerts
     */
    DISPUTE_ALERT
} 