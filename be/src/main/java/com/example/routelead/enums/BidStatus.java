package com.example.routelead.enums;

/**
 * Enum representing the different statuses a bid can have in the RouteLead system.
 */
public enum BidStatus {
    /**
     * Bid is pending review/acceptance
     */
    PENDING,
    
    /**
     * Bid has been accepted
     */
    ACCEPTED,
    
    /**
     * Bid has been rejected
     */
    REJECTED
} 